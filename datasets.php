<h2>Datasets</h2>

<h3>Systematic dataset Test 1 (T1)</h3>

<p>
Our systematic dataset Test 1 (T1) has 13,000 (non-overlapping) genes with numbers of splice forms per gene varying from one to five. Each dataset has 50 million paired-end reads using mouse genome build mm9 (Mouse Genome Sequencing Consortium, et al. 2002).  Data set T1 was designed to determine upper bounds on the accuracies of the methods by providing data as ideal as possible.  Three different types of alternate splicing were included: (i) exon skipping, (ii) alternate 3’/5’ site (start/end of transcription) and (iii) alternate splice sites at the ends of internal exons.  The vast majority of alternate splicing events can be explained by these three categories.  The basic forms for dataset T1 were taken from real Ref-Seq genes (Pruitt KD, et al.  2014) as aligned to the reference genome by the UCSC genome browser (Kent WJ, et al. 2002).  The first 1,000 genes were chosen to have one splice form and at least 5 exons each.  For each of the three categories of alternate splicing there are an additional 4,000 genes with 1,000 having two forms, 1,000 having three forms, 1,000 having four forms and 1,000 having five forms.  In this data set we did not mix differ-ent types of alternate splicing in the same gene.  In other words, for each gene all forms differ by splicing types of just one of the three categories.  In total there are 13,000 genes comprising 43,000 splice forms. The alternate splice forms were generated by randomly removing some exons (exon skipping), or by randomly leaving exons off one or both ends of the tran-script (truncation), or by randomly altering the start or end splice site of individual exons (mostly by multiples of three as is typically observed in real data).  The reads for Test 1 were generated with the following parame-ters: read length=100, fragment length min=200, fragment length max=500, fragment length median=300, basewise error=0%, substitution frequen-cy=0%, indel frequency=0%, intron frequency=0%.  All transcripts were expressed at the same high level with at approximately 40X coverage. We take fragmentation into account yielding data that differ from strictly uni-form by the randomness of the fragmentation process.  The results of this data set should effectively give bounds on the accuracy of the various methods, since no method could do better in reality than it does on perfect data.
</p>
