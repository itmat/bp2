<h3>ENSEMBL Perfect (EP) and ENSEMBL Realistic (ER)</h3>

<p>
Two additional simulated data sets were generated to assess the effect of polymorphisms and sequencing artifacts.  Data set EP (ENSEMBL Perfect) was generated with the same parameters as T1, except the full set of 93,778 unmodified ENSEMBL transcript models was used.  Data set ER (ENSEMBL Realistic) was generated with the same parameters except for the following changes, chosen to mimic real data: basewise error=0,5%, substitution frequency=0.1%, indel frequency=0.05%, intron frequen-cy=30%.  These are fairly low polymorphism rates that would be expected if comparing human sequencing data to the human reference genome.  In reality the polymorphism rates in other organisms will be much higher.  We chose these parameters in order to attain reasonable lower bounds on algo-rithm accuracy in practice, where human/human comparisons are currently among the least polymorphic of all vertebrates.  Intron frequency of 30% may seem high at first, but it is typical.  However, in spite of comprising nearly a third of the reads, since introns are so much larger than exons the majority of intron signal results in very low coverage.  Two-thirds of all transcripts are expressed above zero, with levels of expressed transcripts given by an exponential distribution.  Short genes will be under-represented because of the fragment length distribution so we removed all genes under 200 bases.
</p>
